package hello.model;

public class ProdField {
	
	private String bugNumber;
	private String productField;
	private String componentError;
	private String version;
	private String severity;
	private String hardwareInfected;
	private String operatingSystem;
	private String summary;
	private String description;
	
	public String getBugNumber() {
		return bugNumber;
	}
	public void setBugNumber(String bugNumber) {
		this.bugNumber = bugNumber;
	}
	public String getProductField() {
		return productField;
	}
	public void setProductField(String productField) {
		this.productField = productField;
	}
	public String getComponentError() {
		return componentError;
	}
	public void setComponentError(String componentError) {
		this.componentError = componentError;
	}
	public String getVersion() {
		return version;
	}
	public void setVersion(String version) {
		this.version = version;
	}
	public String getSeverity() {
		return severity;
	}
	public void setSeverity(String severity) {
		this.severity = severity;
	}
	public String getHardwareInfected() {
		return hardwareInfected;
	}
	public void setHardwareInfected(String hardwareInfected) {
		this.hardwareInfected = hardwareInfected;
	}
	public String getOperatingSystem() {
		return operatingSystem;
	}
	public void setOperatingSystem(String operatingSystem) {
		this.operatingSystem = operatingSystem;
	}
	public String getSummary() {
		return summary;
	}
	public void setSummary(String summary) {
		this.summary = summary;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
		
}
